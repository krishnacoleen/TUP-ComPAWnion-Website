                <div class="about-section">
                    <h2>Our Story</h2>
                    <p>TUP ComPAWnion is a student association at the Technological University of the Philippines, Manila. It was established in 2023 by Mariah Arvey Villanueva, currently an alumni and Engineer. What started as a project of ComPAWnion has become an official organization committed to promoting the welfare, protection, and improvement of feline inhabitants of the TUP community.</p>                 
                    <p>The organization seeks to be the voice and advocate for feline pets on campus, championing their rights as well as that of the humane relationship between humans and animals in TUP. Although it is a fairly new organization established in 2023, TUP ComPAWnion has gained wide reach during the year, owing to the kind hearts of fur-raising students.</p>
                </div>
                
                <div class="about-section">
                    <h2>Our Mission</h2>
                    <p>TUP ComPAWnion is committed to enhancing the welfare of stray cats within the university community in Manila. We strive for better lives through awareness and community engagement. Our initiative focuses on raising adoption rates and gathering generous donations to care for these cats. By showcasing our projects, we encourage students and the public to participate and adopt. </p>
                </div>
                
                <div class="about-section">
                    <h2>Our Vision</h2>
                    <p>The purpose of this Organization is to advocate for the welfare, protection, and betterment of feline residents within the Technological University of the Philippines (TUP) community. The Organization aims to serve as the voice and champion for these feline companions, promote their rights, and foster a harmonious relationship between humans and animals on campus.</p>
                </div>
                
