                    <h2>Our Activities</h2>
                    <div class="activities-list">
                        <div class="activity-item">
                            <h3><i class="fas fa-bowl-food"></i> Feeding Program</h3>
                            <p>Daily feeding of campus cats at designated feeding stations with monitored nutrition and health checks.</p>
                        </div>
                        <div class="activity-item">
                            <h3><i class="fas fa-ambulance"></i> Rescue Operations</h3>
                            <p>Responding to reports of injured or distressed cats with trained emergency response teams.</p>
                        </div>
                        <div class="activity-item">
                            <h3><i class="fas fa-stethoscope"></i> Veterinary Care</h3>
                            <p>Arranging for vaccinations, spaying/neutering, and medical treatment through partner veterinarians.</p>
                        </div>
                        <div class="activity-item">
                            <h3><i class="fas fa-home"></i> Adoption Program</h3>
                            <p>Finding forever homes for adoptable cats through screening, matching, and follow-up support.</p>
                        </div>
                        <div class="activity-item">
                            <h3><i class="fas fa-graduation-cap"></i> Education & Outreach</h3>
                            <p>Raising awareness about responsible pet ownership and animal welfare through campus events and workshops.</p>
                        </div>
                        <div class="activity-item">
                            <h3><i class="fas fa-hand-holding-heart"></i> Community Engagement</h3>
                            <p>Building partnerships with TUP administration, local businesses, and community organizations.</p>
